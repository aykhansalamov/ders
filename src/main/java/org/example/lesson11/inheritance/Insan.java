package org.example.lesson11.inheritance;

public class Insan {
    public void yemek (String et, String su, String meyve) {
        System.out.println("Insan: " + et + ", " + su + ", " + meyve + " yeyir");
    }

    public void nefesAlmaq (String hava) {
        System.out.println("Insan: " + hava + " ile"+ " nefes alir");
    }
    public void yatmaq (String yataq) {
        System.out.println("Insan " + yataq + "-da yatir.");
    }
}
