package org.example.lesson3;

public class AssignmentOperators {
//    +=
//    -+
    public static void main(String[] args) {
        int num = 10;
        num += 5; // num = num + 5
        System.out.println(num); // 15

        num -= 3; // num = num - 3
        System.out.println(num);  // 12
    }
}
